num1 = float(input("Digite o primeiro número: "))  # solicita ao usuário o primeiro número e armazena na variável 'num1'
num2 = float(input("Digite o segundo número: "))  # solicita ao usuário o segundo número e armazena na variável 'num2'

soma = num1 + num2  # calcula a soma dos dois números e armazena na variável 'soma'

print("A soma de", num1, "+", num2, "é igual a", soma)  # exibe a mensagem com o resultado da soma
